# CALLBACK HANDLER
# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ
async def on_callback(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    q    = update.callback_query
    cid  = q.message.chat_id
    user = update.effective_user
    await q.answer()
    data = q.data

    if data == "noop":
        return

    s = get_settings(cid, user.username or "", user.first_name or "")

    # в”Җ Cycle a setting в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    if data.startswith("cycle:"):
        key  = data.split(":")[1]
        newv = cycle_val(s[key], key)
        update_setting(cid, key, newv)
        s    = get_settings(cid)
        await q.edit_message_text(
            _settings_text(s),
            parse_mode=ParseMode.MARKDOWN_V2,
            reply_markup=settings_keyboard(s)
        )

    # в”Җ Toggle boolean в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data.startswith("toggle:"):
        key  = data.split(":")[1]
        newv = 0 if s[key] else 1
        update_setting(cid, key, newv)
        s    = get_settings(cid)
        await q.edit_message_text(
            _settings_text(s),
            parse_mode=ParseMode.MARKDOWN_V2,
            reply_markup=settings_keyboard(s)
        )

    # в”Җ Settings panel в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data in ("show:settings", "show:settings_inline"):
        await q.edit_message_text(
            _settings_text(s),
            parse_mode=ParseMode.MARKDOWN_V2,
            reply_markup=settings_keyboard(s)
        )

    elif data == "settings:close":
        await q.edit_message_text(
            "вң… *Settings saved\\!*\n\nSend your manga pages now\\.",
            parse_mode=ParseMode.MARKDOWN_V2,
            reply_markup=main_keyboard()
        )

    elif data == "settings:reset":
        for k, v in [("lang","en"),("voice","calm"),("style","cinematic"),
                     ("subtitles",1),("speed","normal"),("quality","hd"),
                     ("color_grade","vivid"),("bg_blur",0)]:
            update_setting(cid, k, v)
        s = get_settings(cid)
        await q.edit_message_text(
            "рҹ”„ *Settings reset to defaults\\!*\n\n" + _settings_text(s),
            parse_mode=ParseMode.MARKDOWN_V2,
            reply_markup=settings_keyboard(s)
        )

    # в”Җ Help в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "show:help":
        await q.message.reply_text(
            "Use /help for the full guide\\.", parse_mode=ParseMode.MARKDOWN_V2
        )

    # в”Җ Stats в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "show:stats":
        d = db_stats(cid)
        await q.answer(
            f"рҹҺ¬ Videos: {d['video_count']} | рҹ‘Ҙ Users: {d['total_users']}",
            show_alert=True
        )

    # в”Җ Formats info в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "show:formats":
        await q.answer(
            "Supported: JPG В· PNG В· WEBP В· PDF В· ZIP\nMax 20 pages per video",
            show_alert=True
        )

    # в”Җ Demo mode в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "show:demo":
        await q.answer(
            "рҹҺ¬ Demo mode coming soon!\nSend your own manga for now.",
            show_alert=True
        )

    # в”Җ Confirm processing в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "confirm_process:go":
        await q.edit_message_text("вҸі *Starting video generationвҖҰ*", parse_mode=ParseMode.MARKDOWN_V2)
        ctx.user_data["status_msg_id"] = None
        asyncio.create_task(run_pipeline(ctx.bot, cid, ctx, q.message.message_id))

    # в”Җ Cancel в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
    elif data == "cancel_process":
        ctx.user_data.pop("pending_files", None)
        ctx.user_data.pop("status_msg_id", None)
        await q.edit_message_text("вқҢ *Cancelled\\.*", parse_mode=ParseMode.MARKDOWN_V2)

    elif data == "add_more":
        await q.answer("рҹ“ё Send more pages! Tap Generate when done.", show_alert=False)

# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ
# PIPELINE RUNNER
# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ
async def run_pipeline(bot, cid: int, ctx: ContextTypes.DEFAULT_TYPE, msg_id: int):
    if ctx.user_data.get("processing"):
        return
    ctx.user_data["processing"] = True

    files = ctx.user_data.pop("pending_files", [])
    if not files:
        await bot.send_message(cid, "вҡ пёҸ No files queued. Send some manga pages first!")
        ctx.user_data["processing"] = False
        return

    s = db_get(cid) or {}

    # Animated progress message
    progress = await bot.send_message(
        cid,
        "```\nрҹҺ¬ MANGAVOICE ULTRA вҖ” Processing\n"
        "в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n"
        "[ в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘ ]  0%\n"
        "Initialising pipelineвҖҰ\n```",
        parse_mode=ParseMode.MARKDOWN_V2
    )

    async def update_progress(pct: int, label: str):
        filled = int(pct / 5)
        bar    = "в–Ҳ" * filled + "в–‘" * (20 - filled)
        try:
            await bot.edit_message_text(
                f"```\nрҹҺ¬ MANGAVOICE ULTRA вҖ” Processing\n"
                f"в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ\n"
                f"[ {bar} ]  {pct}%\n"
                f"{label}\n```",
                chat_id=cid,
                message_id=progress.message_id,
                parse_mode=ParseMode.MARKDOWN_V2
            )
        except Exception:
            pass

    try:
        from pipeline import MangaPipeline
        pipeline = MangaPipeline(
            bot=bot,
            chat_id=cid,
            update_progress=update_progress,
            settings=s,
            anthropic_key=ANTHROPIC_KEY,
            elevenlabs_key=ELEVENLABS_KEY,
        )
        output_video, page_count = await pipeline.run(files)

        db_log_video(cid, page_count, s.get("style","cinematic"), s.get("lang","en"))

        style_label = STYLE_ICONS.get(s.get("style","cinematic"), "рҹҺ¬ Cinematic")
        caption = (
            "рҹҺҢ *Your Manga Video is Ready\\!*\n\n"
            f"рҹ“„ Pages     : `{page_count}`\n"
            f"рҹҺ¬ Style     : `{style_label}`\n"
            f"рҹҺӨ Voice     : `{VOICE_ICONS.get(s.get('voice','calm'), 'рҹ§ҳ Calm')}`\n"
            f"рҹҢҗ Language  : `{LANG_FLAGS.get(s.get('lang','en'), 'рҹҮ¬рҹҮ§ English')}`\n"
            f"рҹ“қ Subtitles : `{'ON вң…' if s.get('subtitles') else 'OFF вқҢ'}`\n\n"
            "_Enjoy your cinematic manga experience\\!_ рҹҚҝ"
        )

        await bot.send_video(
            chat_id=cid,
            video=open(output_video, "rb"),
            caption=caption,
            parse_mode=ParseMode.MARKDOWN_V2,
            supports_streaming=True,
            width=1920 if s.get("quality") == "4k" else 1280,
            height=1080 if s.get("quality") == "4k" else 720,
        )

        try:
            await bot.delete_message(cid, progress.message_id)
        except Exception:
            pass

        Path(output_video).unlink(missing_ok=True)

    except ImportError as e:
        await bot.edit_message_text(
            f"вҡ пёҸ *Missing dependency:* `{e}`\n\nInstall: `pip install {str(e).split()[-1]}`",
            chat_id=cid, message_id=progress.message_id,
            parse_mode=ParseMode.MARKDOWN_V2
        )
    except Exception as e:
        logger.exception("Pipeline failed")
        await bot.edit_message_text(
            f"вқҢ *Processing failed:*\n`{str(e)[:300]}`",
            chat_id=cid, message_id=progress.message_id,
            parse_mode=ParseMode.MARKDOWN_V2
        )
    finally:
        ctx.user_data["processing"] = False

