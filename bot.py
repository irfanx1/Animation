async def cmd_testkey(update: Update, ctx: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("рҹ”‘ Testing your Anthropic API keyвҖҰ")
    key = ANTHROPIC_KEY.strip()

    if not key:
        await update.message.reply_text(
            "вқҢ *ANTHROPIC\\_API\\_KEY is empty in config\\.json\\!*\n\n"
            "Add your key and restart the bot\\.",
            parse_mode=ParseMode.MARKDOWN_V2
        )
        return
    if not key.startswith("sk-ant-"):
        await update.message.reply_text(
            f"вқҢ *Key looks wrong:* `{key[:15]}вҖҰ`\n\n"
            "It must start with `sk\\-ant\\-`\n"
            "Copy it fresh from console\\.anthropic\\.com",
            parse_mode=ParseMode.MARKDOWN_V2
        )
        return

    import requests as req
    try:
        r = req.post(
            "https://api.anthropic.com/v1/messages",
            headers={
                "x-api-key": key,
                "anthropic-version": "2023-06-01",
                "content-type": "application/json",
            },
            json={
                "model": "claude-haiku-4-5-20251001",
                "max_tokens": 10,
                "messages": [{"role": "user", "content": "Say OK"}],
            },
            timeout=15,
        )
        if r.status_code == 200:
            await update.message.reply_text(
                "вң… *API key is working perfectly\\!*\n\nYou can now send manga pages\\.",
                parse_mode=ParseMode.MARKDOWN_V2
            )
        elif r.status_code == 401:
            await update.message.reply_text(
                "вқҢ *401 Unauthorized*\n\n"
                "Your key is being rejected\\. Most common causes:\n\n"
                "1\\. *No billing added* вҶ’ go to console\\.anthropic\\.com/billing\n"
                "2\\. *Wrong key* вҶ’ create a fresh one at console\\.anthropic\\.com/api\\-keys\n"
                "3\\. *Extra spaces* in config\\.json around the key\n\n"
                "After fixing, restart the bot and run /testkey again\\.",
                parse_mode=ParseMode.MARKDOWN_V2
            )
        elif r.status_code == 403:
            await update.message.reply_text(
                "вқҢ *403 Forbidden* вҖ” Add billing at console\\.anthropic\\.com/billing",
                parse_mode=ParseMode.MARKDOWN_V2
            )
        else:
            await update.message.reply_text(
                f"вҡ пёҸ *API Error {r.status_code}:*\n`{r.text[:300]}`",
                parse_mode=ParseMode.MARKDOWN_V2
            )
    except Exception as e:
        await update.message.reply_text(
            f"вқҢ Connection error: `{str(e)[:200]}`",
            parse_mode=ParseMode.MARKDOWN_V2
        )

